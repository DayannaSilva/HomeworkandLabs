    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String[] dataOutput = null;
         String aux = FileManager.searchInFile(new File("Provider.csv"), txtRUC1.getText());
         if(!aux.equals("0")){
             dataOutput = FileManager.dataSplit(aux);
             txtName.setText(dataOutput[0]);
             txtCompany.setText(dataOutput[1]);
             txtRUC.setText(dataOutput[2]);
             txtMobile.setText(dataOutput[3]);
             txtHomePhone.setText(dataOutput[4]);
         }else{
             showMessageDialog(null,"El trabajo no se ha encontrado\nPor favor!! Ingrese el ID bien...");
         }
    }//GEN-LAST:event_btnSearchActionPerformed
    

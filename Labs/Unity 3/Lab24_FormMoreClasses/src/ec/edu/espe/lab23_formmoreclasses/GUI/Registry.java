    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:

        String imput = (txtName.getText()+";"+txtSalary.getText()+";"+txtHoursWorked.getText()+"\n");
        if(!txtHoursWorked.getText().trim().equals("") && !txtName.getText().trim().equals("")
                && !txtSalary.getText().trim().equals("") ){
            FileManager.writeFile(new File("Registro.csv"),imput);
            JOptionPane.showMessageDialog(null, "Agregación Exitosa");
            float salary = calSalary(Float.parseFloat(txtSalary.getText()),Integer.parseInt(txtHoursWorked.getText()));
            float iess = calIess(salary);
            float deductibleSalary = caldeductibleSalary(salary, iess);
            FileManager.writeFile(new File("Sueldos.csv"), txtName.getText()
                    + ";" + salary + ";" + iess +";"+deductibleSalary +"\n");
            JOptionPane.showMessageDialog(null, "Agregación Exitosa");
            cleanField();
            //cleanField();
        }else{
            JOptionPane.showMessageDialog(this, "Llene todos los campos"
                        , "Error al agregar Empleado", JOptionPane.ERROR_MESSAGE);
            
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String texto = "Esto es lo que va a la impresora";
 
		PrintService printService = PrintServiceLookup.lookupDefaultPrintService();
 
 
		DocFlavor flavor = DocFlavor.BYTE_ARRAY.AUTOSENSE;
		DocPrintJob docPrintJob = printService.createPrintJob();
		Doc doc = new SimpleDoc(texto.getBytes(), flavor, null);
		try {
			docPrintJob.print(doc, null);
		} catch (PrintException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
	
    
    }//GEN-LAST:event_jButton1ActionPerformed
    private float calSalary(float money, int hourWork){
        return money * hourWork;
    }
    private float calIess(float salary){
        return salary * 0.0945f;
    }
    private float caldeductibleSalary(float salary,float inptIESS ){
        return salary-inptIESS;
}
    private void cleanField(){
        txtHoursWorked.setText("");
        txtName.setText("");
        txtSalary.setText("");
    }


    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:

        String imput = (txtID.getText()+";"+txtName.getText()+";"+
                txtPrecio.getText()+";"+txtPorcentaje.getText() +"\n");
        if(!txtPrecio.getText().trim().equals("") && !txtID.getText().trim().equals("")
                && !txtName.getText().trim().equals("")&&!txtPorcentaje.getText().trim().equals("") ){
                     
            float valorporcentaje= calPorcetaje(Float.parseFloat(txtPrecio.getText()),
                    Float.parseFloat(txtPorcentaje.getText()));
            float pvp = calPVP(Float.parseFloat(txtPrecio.getText()), valorporcentaje);
           
            FileManager.writeFile(new File("Producto.csv"),imput+ ";" + pvp + "\n");
            JOptionPane.showMessageDialog(null, "Agregaci√≥n Exitosa");
            cleanField();
            //cleanField();
        }else{
            JOptionPane.showMessageDialog(this, "Llene todos los campos"
                        , "Error al agregar Empleado", JOptionPane.ERROR_MESSAGE);
            
        }
        
    }//GEN-LAST:event_btnAddActionPerformed
    private float calPVP(float precio, float porcentajePrecio){
        return precio+(porcentajePrecio%100);
    }
    private float calPorcetaje(float precio, float porcentaje){
        return precio * porcentaje;
    }
    private float caldeductibleSalary(float salary,float inptIESS ){
        return salary-inptIESS;
}
    private void cleanField(){
        txtPrecio.setText("");
        txtID.setText("");
        txtName.setText("");
    }

